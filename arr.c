#include "push_swap.h"

void	make_arr(int ac, char **ar, long long int *a, long long int *b)
{
	int i;

	i = 0;
	while (i < ac - 1)
	{
		a[i] = ft_atoi(ar[i + 1]);
		i++;
	}
	a[i] = 3000000000;
	b[0] = 3000000000;
}
